"""
Disini kita akan belajar operator aritmetika, simplenya operator aritmetika adalah
operator matematika dasar seperti penjumlahan, pengurangan, pembagian, perkalian, perpangkatan, dan ada modulo
"""

#Cara kerjanya sama seperti di matematika
#penjumlahan
g = 10 + 10 #output 20
print(g)
#pengurangan
i = 10 - 10 #output 0
print(i)
#perkalian
b = 10 * 10 #output 100
print(b)
#pembagian
r = 10 / 10 #output 1
print(r)
#perpangkatan
a = 10 ** 2 #output 100
print(a)
#modulo
n = 10 % 3 #output 1
print(n)
#modulo adalah operator yang menghasilkan sisa dari pembagian, 10 / 3 = 3 sisa 1, nah modulo itu adalah 1 nya yaitu sisa dari pembagian
#kalau tidak ada sisa? ya maka hasilnya akan 0 seperti 10 / 1 = 10, tidak ada sisa maka 0
